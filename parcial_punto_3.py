# e) listar todos los Jedi que comienzan con A;
def listar_con_A(jedis):
    lista_A = []
    for jedi in jedis:
        if jedi['name'].startswith('A'):
            lista_A.append(jedi['name'])
    print("estos son los jedis que comienzan con la letra A: ", lista_A)

