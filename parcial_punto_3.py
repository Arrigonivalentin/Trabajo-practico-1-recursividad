# c) mostrar todos los padawan de Yoda y Luke Skywalker, es decir sus aprendices;

def mostrar_padawan(jedis):
    aprendices_yoda = []
    aprendices_luke = []
    for index, aprendices in enumerate(jedis):
        if jedis[index]['master'] is not None:
            if 'Yoda' in jedis[index]['master']:
                aprendices_yoda.append(jedis[index]['name'])
            elif 'Luke Skywalker' in jedis[index]['master']:
                aprendices_luke.append(jedis[index]['name'])
    print("estos son los aprendices de Yoda: ", aprendices_yoda)
    print()
    print("estos son los aprendices de Luke: ", aprendices_luke)
    return()


